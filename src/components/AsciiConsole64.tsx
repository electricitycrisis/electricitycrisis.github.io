export const AsciiConsole64: React.FC = () => {
  useEffect(() => {
    // Split original ASCII into lines
    const lines = originalAscii.trim().split("\n");
    const origHeight = lines.length;
    const origWidth = Math.max(...lines.map((l) => l.length));

    const targetSize = 64;

    // Function to sample characters from original ASCII
    const getChar = (x: number, y: number) => {
      const origX = Math.floor((x / targetSize) * origWidth);
      const origY = Math.floor((y / targetSize) * origHeight);
      return lines[origY][origX] || " ";
    };

    // Build the scaled 64x64 ASCII
    let scaled = "";
    for (let y = 0; y < targetSize; y++) {
      for (let x = 0; x < targetSize; x++) {
        scaled += getChar(x, y);
      }
      scaled += "\n";
    }

    // Print in browser console
    console.log(scaled);
  }, []);

  return <></>; // Empty fragment; nothing rendered in DOM
};
